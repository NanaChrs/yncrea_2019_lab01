package model;

public class Drug {
    private String name;
    private String lab;

    public String getName() {
        return name;
    }

    public String getLab() {
        return lab;
    }

    public void setLab(String lab) {
        this.lab = lab;
    }

    public void setName(String name) {
        this.name = name;

    }
}
